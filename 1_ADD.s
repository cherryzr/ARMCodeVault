// ADDING VALUE TO BUNCHA REGISTERS AND MOVING EM AROUND

.global _start
_start:
	
	MOV R0, #3
	
	// THIS WILL ADD THE DECIMAL VALUE 4 TO THE REGISTER R1
	MOV R1, #4

	// THIS WILL ADD HEXADECIMAL VALUE 4 TO THE REGISTER R2
	MOV R2, #0X04
	
	// THIS WILL ADD THE VALUE OF REGISTER 0 TO R1 AND THEN ENTER THAT VALUE IN R2
	ADD R3, R1, R0